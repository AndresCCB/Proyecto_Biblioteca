    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        try{
            String strNombre = txtNombre.getText();
            String strEditorial = txtEditorial.getText();
            String strAño = txtAño.getText();
            int intAño = Integer.parseInt(strAño);
            String strEstado = (String) cbEstado.getSelectedItem();
            
            Autor autor = new Autor();
            autor = (Autor) cbAutor.getSelectedItem();
            
            System.out.println("El nombre del autor seleccionado es: " +autor.getNombre());
            System.out.println("El id del autor seleccionado es: " + autor.getId_autor());
            
            Libro libro = new Libro(strNombre, autor.getId_autor(), strEditorial, intAño, strEstado);

            LibroDao librodao = new LibroDao();
            boolean bolGuardoDatos = false;
            bolGuardoDatos = librodao.guardar_libro(libro);

            if(!bolGuardoDatos){
                limpiar_tabla();
                JOptionPane.showMessageDialog(this, "Ingreso los datos correctamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                llenar_tabla();
            }
            else{
                JOptionPane.showMessageDialog(this, "NO Ingreso los datos correctamente", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error al ingresar datos", "Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

