    private void generar_CB_autor(){
        cbAutor.removeAllItems();
        
        ArrayList<Autor> arreglo_autores = new ArrayList<Autor>();
        AutorDao autordao = new AutorDao();
        autordao.consultar_autorCB();
        arreglo_autores = autordao.consultar_autorCB();
        Iterator<Autor> iterator_autor = arreglo_autores.iterator();
        
        while(iterator_autor.hasNext()){
            Autor objeto_autor = iterator_autor.next();
            cbAutor.addItem(objeto_autor);
        } 
    }
    
    private void limpiar_tabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) jtbLibro.getModel();
        int total_registro = jtbLibro.getRowCount() - 1;
        for(int i = total_registro; i >= 0; i--){
            //modeloTabla.removeRow(modeloTabla.getRowCount() - 1); 
            modeloTabla.removeRow(i); 
        }          
    }
    
    private void llenar_tabla(){
        DefaultTableModel modeloTabla;
        modeloTabla = (DefaultTableModel)jtbLibro.getModel();
        ArrayList<Libro> arreglo_libro = new ArrayList<Libro>();
        LibroDao librodao = new LibroDao();
        
        arreglo_libro = librodao.consultar_libro();
        Object[] obj_libro = new Object[6];        
        Iterator<Libro> iterator_libro = arreglo_libro.iterator();
        
        while(iterator_libro.hasNext()){ //hasnext para saber si tiene valor, es decir, mientras tenga valores siga en el ciclo.
            Libro objeto_libro = iterator_libro.next();
            obj_libro[0] = objeto_libro.getId_libro();
            obj_libro[1] = objeto_libro.getNombre();
            obj_libro[2] = objeto_libro.getId_autor();
            obj_libro[3] = objeto_libro.getEditorial();
            obj_libro[4] = objeto_libro.getAño();
            obj_libro[5] = objeto_libro.getEstado();
            
            modeloTabla.addRow(obj_libro);
        }
        
        jtbLibro.setModel(modeloTabla);        
    }

