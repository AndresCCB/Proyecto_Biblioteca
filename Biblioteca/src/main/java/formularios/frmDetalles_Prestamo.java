    private void generar_CB_libro(){
        cbLibro.removeAllItems();
        
        ArrayList<Libro> arreglo_libros = new ArrayList<Libro>();
        LibroDao librodao= new LibroDao();
        librodao.consultar_LibroCB();
        arreglo_libros = librodao.consultar_LibroCB();
        Iterator<Libro> iterator_libro = arreglo_libros.iterator();
        
        while(iterator_libro.hasNext()){
            Libro objeto_libro = iterator_libro.next();
            cbLibro.addItem(objeto_libro);
        } 
    }
    
    private void generar_CB_prestamo(){
        cbPrestamo.removeAllItems();
        
        ArrayList<Prestamo> arreglo_prestamos = new ArrayList<Prestamo>();
        PrestamoDao prestamodao = new PrestamoDao();
        prestamodao.consultar_PrestamoCB();
        arreglo_prestamos = prestamodao.consultar_PrestamoCB();
        Iterator<Prestamo> iterator_prestamo = arreglo_prestamos.iterator();
        
        while(iterator_prestamo.hasNext()){
            Prestamo objeto_prestamo = iterator_prestamo.next();
            cbPrestamo.addItem(objeto_prestamo);
        } 
    }
    
    private void limpiar_tabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) jtbDetalles.getModel();
        int total_registro = jtbDetalles.getRowCount() - 1;
        for(int i = total_registro; i >= 0; i--){
            //modeloTabla.removeRow(modeloTabla.getRowCount() - 1); 
            modeloTabla.removeRow(i); 
        }          
    }
    
    private void llenar_tabla(){
        DefaultTableModel modeloTabla;
        modeloTabla = (DefaultTableModel)jtbDetalles.getModel();
        ArrayList<Detalles_prestamo> arreglo_Detalles = new ArrayList<Detalles_prestamo>();
        Detalles_prestamoDao detallesdao = new Detalles_prestamoDao();
        
        arreglo_Detalles = detallesdao.consultar_prestamo();
        Object[] obj_Detalles = new Object[3];        
        Iterator<Detalles_prestamo> iterator_detalles = arreglo_Detalles.iterator();
        
        while(iterator_detalles.hasNext()){ //hasnext para saber si tiene valor, es decir, mientras tenga valores siga en el ciclo.
            Detalles_prestamo objeto_prestamo = iterator_detalles.next();
            obj_Detalles[0] = objeto_prestamo.getId_libro();
            obj_Detalles[1] = objeto_prestamo.getId_prestamo();
            obj_Detalles[2] = objeto_prestamo.getFecha_devolucion();
            
            
            modeloTabla.addRow(obj_Detalles);
        }
        
        jtbDetalles.setModel(modeloTabla);        
    }

