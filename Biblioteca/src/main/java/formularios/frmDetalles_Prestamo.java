    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try{
            String strFechaDevolucion = txtFechaDev.getText();
            Date datFechaDevolucion = null;
            
            Libro libro = new Libro();
            libro = (Libro) cbLibro.getSelectedItem();
            
            System.out.println("El nombre del libro seleccionado es: " + libro.getNombre());
            System.out.println("El id del libro seleccionado es: " + libro.getId_libro());
            
            Prestamo prestamo = new Prestamo();
            prestamo = (Prestamo) cbPrestamo.getSelectedItem();
            
            System.out.println("El id del prestamo es: " + prestamo.getId_prestamo());
            System.out.println("El id del socio que hizo este prestamo es: " + prestamo.getId_socio()); 
            
            try{
                datFechaDevolucion = new SimpleDateFormat("dd/MM/yyyy").parse(strFechaDevolucion);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, "Error al convertir fecha " + e, "Error", JOptionPane.ERROR_MESSAGE);
            }

            Detalles_prestamo detalles_prestamo = new Detalles_prestamo(libro.getId_libro(), prestamo.getId_prestamo() , datFechaDevolucion);
            
            Detalles_prestamoDao detalles_prestamodao = new Detalles_prestamoDao();
            boolean bolGuardoDatos = false;
            bolGuardoDatos = detalles_prestamodao.guardar_detallesPR(detalles_prestamo);

            if(!bolGuardoDatos){
                limpiar_tabla();
                JOptionPane.showMessageDialog(this, "Ingreso los datos correctamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                llenar_tabla();
            }
            else{
                JOptionPane.showMessageDialog(this, "NO Ingreso los datos correctamente", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error al ingresar datooos " + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

