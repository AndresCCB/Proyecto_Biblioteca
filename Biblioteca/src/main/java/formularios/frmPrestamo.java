    private void generar_CB_socio(){
        cbSocio.removeAllItems();
        
        ArrayList<Socio> arreglo_socios = new ArrayList<Socio>();
        SocioDao sociodao= new SocioDao();
        sociodao.consultar_SocioCB();
        arreglo_socios = sociodao.consultar_SocioCB();
        Iterator<Socio> iterator_socio = arreglo_socios.iterator();
        
        while(iterator_socio.hasNext()){
            Socio objeto_socio = iterator_socio.next();
            cbSocio.addItem(objeto_socio);
        } 
    }
    
    private void limpiar_tabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) jtbSocio.getModel();
        int total_registro = jtbSocio.getRowCount() - 1;
        for(int i = total_registro; i >= 0; i--){
            //modeloTabla.removeRow(modeloTabla.getRowCount() - 1); 
            modeloTabla.removeRow(i); 
        }          
    }
    
    private void llenar_tabla(){
        DefaultTableModel modeloTabla;
        modeloTabla = (DefaultTableModel)jtbSocio.getModel();
        ArrayList<Prestamo> arreglo_prestamo = new ArrayList<Prestamo>();
        PrestamoDao prestamodao = new PrestamoDao();
        
        arreglo_prestamo = prestamodao.consultar_prestamo();
        Object[] obj_prestamo = new Object[4];        
        Iterator<Prestamo> iterator_autor = arreglo_prestamo.iterator();
        
        while(iterator_autor.hasNext()){ //hasnext para saber si tiene valor, es decir, mientras tenga valores siga en el ciclo.
            Prestamo objeto_prestamo = iterator_autor.next();
            obj_prestamo[0] = objeto_prestamo.getId_prestamo();
            obj_prestamo[1] = objeto_prestamo.getFecha_entrega();
            obj_prestamo[2] = objeto_prestamo.getId_socio();
            obj_prestamo[3] = objeto_prestamo.getCantidad_libros();
            
            modeloTabla.addRow(obj_prestamo);
        }
        
        jtbSocio.setModel(modeloTabla);        
    }

