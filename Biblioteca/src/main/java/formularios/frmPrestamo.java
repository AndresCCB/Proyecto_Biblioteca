    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try{
            String strCantLibros = txtCantLibros.getText();
            int intCantLibros = Integer.parseInt(strCantLibros);
            String strFechaEntrega = txtFechaEntrega.getText();
            Date datFechaEntrega = null;
            
            Socio socio = new Socio();
            socio = (Socio) cbSocio.getSelectedItem();
            
            System.out.println("El nombre del socio seleccionado es: " + socio.getNombre());
            System.out.println("El id del socio seleccionado es: " + socio.getId_socio());
            
            
            
            try{
                datFechaEntrega = new SimpleDateFormat("dd/MM/yyyy").parse(strFechaEntrega);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, "Error al convertir fecha " + e, "Error", JOptionPane.ERROR_MESSAGE);
            }

            Prestamo prestamo = new Prestamo(datFechaEntrega, socio.getId_socio(), intCantLibros);
            
            PrestamoDao prestamodao = new PrestamoDao();
            boolean bolGuardoDatos = false;
            bolGuardoDatos = prestamodao.guardar_prestamo(prestamo);

            if(!bolGuardoDatos){
                limpiar_tabla();
                JOptionPane.showMessageDialog(this, "Ingreso los datos correctamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                llenar_tabla();
            }
            else{
                JOptionPane.showMessageDialog(this, "NO Ingreso los datos correctamente", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error al ingresar datooos " + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

