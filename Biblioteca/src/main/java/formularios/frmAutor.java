    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try{
            String strNombre = txtNombre.getText();
            String strNacionalidad = txtNacionalidad.getText();
            String strFechaNacimiento = txtFechaNacimiento.getText();
            Date datFechaNacimiento = null;
            try{
                datFechaNacimiento = new SimpleDateFormat("dd/MM/yyyy").parse(strFechaNacimiento);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, "Error al convertir fecha " + e, "Error", JOptionPane.ERROR_MESSAGE);
            }

            Autor autor = new Autor(strNombre, strNacionalidad, datFechaNacimiento);

            AutorDao autordao = new AutorDao();
            boolean bolGuardoDatos = false;
            bolGuardoDatos = autordao.guardar_autor(autor);

            if(!bolGuardoDatos){
                limpiar_tabla();
                JOptionPane.showMessageDialog(this, "Ingreso los datos correctamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                llenar_tabla();
            }
            else{
                JOptionPane.showMessageDialog(this, "NO Ingreso los datos correctamente", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error al ingresar datos", "Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

