    private void limpiar_tabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) jtbAutor.getModel();
        int total_registro = jtbAutor.getRowCount() - 1;
        for(int i = total_registro; i >= 0; i--){
            //modeloTabla.removeRow(modeloTabla.getRowCount() - 1); 
            modeloTabla.removeRow(i); 
        }          
    }
    
    private void llenar_tabla(){
        DefaultTableModel modeloTabla;
        modeloTabla = (DefaultTableModel)jtbAutor.getModel();
        ArrayList<Autor> arreglo_autor = new ArrayList<Autor>();
        AutorDao autordao = new AutorDao();
        
        arreglo_autor = autordao.consultar_autor();
        Object[] obj_autor = new Object[4];        
        Iterator<Autor> iterator_autor = arreglo_autor.iterator();
        
        while(iterator_autor.hasNext()){ //hasnext para saber si tiene valor, es decir, mientras tenga valores siga en el ciclo.
            Autor objeto_autor = iterator_autor.next();
            obj_autor[0] = objeto_autor.getId_autor();
            obj_autor[1] = objeto_autor.getNombre();
            obj_autor[2] = objeto_autor.getNacionalidad();
            obj_autor[3] = objeto_autor.getFecha_nacimiento();
            
            modeloTabla.addRow(obj_autor);
        }
        
        jtbAutor.setModel(modeloTabla);        
    }
    
