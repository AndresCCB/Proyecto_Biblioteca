    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try{
            String strId = txtId.getText();
            int intId = Integer.parseInt(strId);
            String strNombre = txtNombre.getText();
            String strTelefono = txtTelefono.getText();
            String strDireccion = txtDireccion.getText();
            String strEmail = txtEmail.getText();
            
            Socio socio = new Socio(intId, strNombre, strTelefono, strDireccion, strEmail);

            SocioDao sociodao = new SocioDao();
            boolean bolGuardoDatos = false;
            bolGuardoDatos = sociodao.guardar_Socio(socio);

            if(!bolGuardoDatos){
                limpiar_tabla();
                JOptionPane.showMessageDialog(this, "Ingreso los datos correctamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                llenar_tabla();
            }
            else{
                JOptionPane.showMessageDialog(this, "NO Ingreso los datos correctamente", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error al ingresar datos", "Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

