    private void limpiar_tabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) jtbSocio.getModel();
        int total_registro = jtbSocio.getRowCount() - 1;
        for(int i = total_registro; i >= 0; i--){
            //modeloTabla.removeRow(modeloTabla.getRowCount() - 1); 
            modeloTabla.removeRow(i); 
        }          
    }
    
    private void llenar_tabla(){
        DefaultTableModel modeloTabla;
        modeloTabla = (DefaultTableModel)jtbSocio.getModel();
        ArrayList<Socio> arreglo_socio = new ArrayList<Socio>();
        SocioDao sociodao = new SocioDao();
        
        arreglo_socio = sociodao.consultar_socio();
        Object[] obj_socio = new Object[6];        
        Iterator<Socio> iterator_socio = arreglo_socio.iterator();
        
        while(iterator_socio.hasNext()){ //hasnext para saber si tiene valor, es decir, mientras tenga valores siga en el ciclo.
            Socio objeto_socio = iterator_socio.next();
            obj_socio[0] = objeto_socio.getId_socio();
            obj_socio[1] = objeto_socio.getNombre();
            obj_socio[2] = objeto_socio.getTelefono();
            obj_socio[3] = objeto_socio.getDireccion();
            obj_socio[4] = objeto_socio.getEmail();
            
            modeloTabla.addRow(obj_socio);
        }
        
        jtbSocio.setModel(modeloTabla);        
    }

